import numpy as np

from ..linear_homogenization import LinearHomogenization


def test_constructor():
    lambda_ = 10
    mu = 10
    voxel = np.ones((10, 10, 10))
    h = LinearHomogenization(lambda_, mu, voxel)

    assert h.void_material
    assert np.array_equal(h.voxel, voxel)
    assert np.array_equal(h.constitutive_tensor, np.zeros((6, 6)))


def test_10x10_uniform_solver():
    lambda_ = 10
    mu = 10
    voxel = np.ones((10, 10, 10))
    h = LinearHomogenization(lambda_, mu, voxel)

    assert h.void_material
    assert np.array_equal(h.voxel, voxel)
    assert np.array_equal(h.constitutive_tensor, np.zeros((6, 6)))

    h.solve()
    comp = np.array(
        [
            [30, 10, 10, 0, 0, 0],
            [10, 30, 10, 0, 0, 0],
            [10, 10, 30, 0, 0, 0],
            [0, 0, 0, 10, 0, 0],
            [0, 0, 0, 0, 10, 0],
            [0, 0, 0, 0, 0, 10],
        ]
    )
    assert np.isclose(h.constitutive_tensor, comp).all()


